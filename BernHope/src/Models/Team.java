package Models;

/**
 *
 * @author Bruno
 */
public class Team {
    
    private int id;
    private String name;

    public Team() {
    }

    public Team(int id, String nome) {
        this.id = id;
        this.name = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }    
}
